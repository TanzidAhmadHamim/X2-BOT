function startBot(message) {
    (message) ? logger(message, "• X2 Starting•••") : "";

    const child = spawn("node", ["--trace-warnings", "--async-stack-traces", "H4M1MX2.js"], {
        cwd: __dirname,
        stdio: "inherit",
        shell: true
    });

    child.on("close", (codeExit) => {
        if (codeExit != 0 || global.countRestart && global.countRestart < 5) {
            startBot("X2 FACING A ERROR TRYING TO RESTART•••");
            global.countRestart += 1;
            return;
        } else return;
    });

    child.on("error", function (error) {
        logger("An error occurred: " + JSON.stringify(error), "• X2 Starting •");
    });
};
